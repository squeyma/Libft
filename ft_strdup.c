#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tab;
	size_t	i;

	i = 0;
	tab = malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (!tab || !s1)
		return (NULL);
	while (s1[i])
	{
		tab[i] = s1[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
